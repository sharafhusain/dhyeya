                <h3>Q. What is the educational qualification needed to appear in IAS exam?</h3>
                <p><strong>Answer:</strong>&nbsp;Any degree (graduation) which may be regular or distant. The candidate
                    must hold a degree from any of Universities incorporated by an Act of the Central or State
                    Legislature in India or other educational institutions established by an Act of Parliament or
                    declared to be deemed as a University Under Section-3 of the University Grants Commission Act, 1956,
                    or possess an equivalent qualification.</p>
                <h3>Q. Can final year students of graduation apply for UPSC CSE?</h3>
                <p><strong>Answer:</strong>&nbsp;Yes, candidates who have appeared at an examination the passing of
                    which would render them educationally qualified for the Commissions examination but have not been
                    informed of the results as also the candidates who intend to appear at such a qualifying examination
                    will also be eligible for admission to the Preliminary Examination.</p>
                <h3>Q. When should I produce the proof of passing my graduation examination before UPSC?</h3>
                <p><strong>Answer:</strong>&nbsp;All candidates who are declared qualified by the Commission for taking
                    the Civil Services (Main) Examination will be required to produce proof of passing the requisite
                    examination with their application for the Main Examination failing which such candidates will not
                    be admitted to the Main Examination.</p>
                <h3>Q. I hesitate to apply for Civil Services Exam because I cannot speak fluently in English. Is it
                    possible that I write the Civil Services Main Exam in English and take the interview in Hindi or in
                    any other India Language?</h3>
                <p><strong>Answer:</strong>&nbsp;You need not be afraid of applying for the Civil Services Exam because
                    UPSC give following options in this respect:</p>
                <ul>
                    <li>If you opt to write the Civil Services Main Exam in English, you may choose either english as
                        the medium for interview or Hindi or any other Indian language opted by you for the compulsory
                        Indian Language Paper in the written part of civil services mains examination as the medium for
                        interview. However, if you are exempted from the Compulsory Indian Language paper, you will have
                        to choose either English or Hindi as medium of interview.
                    </li>
                    <li>If you opt for Indian Language medium for the written part of the Civil Services Main Exam, you
                        can choose either the same Indian Language or English or Hindi as the medium for the Interview
                        or Personality Test.
                    </li>
                </ul>
                <h3>Q. If I apply for the Civil Services Prelims Exam but do not appear in any paper will it be counted
                    as an attempt?</h3>
                <p><strong>Answer:</strong>&nbsp;No, an attempt will be counted only if you have appeared in at least
                    one paper.</p>
                <h3>Q. If a candidate belongs to a community included in the OBC list of states but not in the Central
                    list of OBCs is he eligible for age relaxation, reservation etc. for Civil Services
                    Examinations?</h3>
                <p><strong>Answer:</strong>&nbsp;No, only candidates belonging to communities which are included in the
                    Central list of OBC are eligible for such concessions.</p>
                <h3>Q. Can I choose an optional subject (in Mains), which I have not studied at Graduate/PG level?</h3>
                <p><strong>Answer:</strong>&nbsp;Yes, you can choose any optional subject from amongst the UPSC list of
                    optional subjects for Civil Services Main Exam.</p>
                <h3>Q. Can I write different papers of Civil Services Main Exam. in different languages?</h3>
                <p><strong>Answer:</strong>&nbsp;No, you have the option to write your answers either in English or in
                    any one of the languages included in the Eighth schedule to Constitution.</p>
                <h3>Q. Generally, it is advised that the candidates should carefully study the last 10 years question
                    papers of General Studies (Prelims) exam as these give a fair idea as to how the questions are
                    framed from the respective themes of the syllabus and also indicate the difficulty level.</h3>
                <p><strong>Answer:</strong>&nbsp;Yes, previous years papers help the candidates to know the trend, and
                    they must go through these papers again and again.</p>
                <h3>Q. Are individual marks secured in various papers or aggregate marks across all papers considered f
                    or merit?</h3>
                <p><strong>Answer:&nbsp;</strong>Total marks are considered.</p>
                <h3>Q. How tough is the competition in UPSC Civil Services Examination (CSE)?</h3>
                <p><strong>Answer:</strong>&nbsp;You can assess the level of competition from the following data:</p>
                <ul>
                    <li>No. of vacancies advertised every year : Between 1000 and 1200.</li>
                    <li>No. of candidates who filled the form : More than 9,00,000</li>
                    <li>No. of applications who appeared in the Preliminary exam : Almost 4,50,000-5,00,000</li>
                    <li>No. of candidates who qualify the Prelims and become eligible to appear in the Mains Exam :
                        Equal to 12 to 13 times the nos. of vacancies of CSE.
                    </li>
                    <li>Nos. of Candidates who qualify Mains to appear in the Interview : 2-2&frac12; times the Nos. of
                        vacancies in the CSE. Thus, one can say that CSE is one of the tough est competitive
                        examinations.
                    </li>
                </ul>
                <h3>Q. Will there be any exceptions to the above-mentioned educational requirements?</h3>
                <p><strong>Answer:</strong>&nbsp;In exceptional cases the Union Public Service Commission may treat a
                    candidate who has not any of the foregoing qualifications as a qualified candidate provided that
                    he/she has passed examination conducted by the other Institutions, the standard of which in the
                    opinion of the Commission justifies his/her admission to the examination.</p>
                <h3>Q. I possess professional/technical qualification. Am I eligible to appear for UPSC CSE?</h3>
                <p><strong>Answer:&nbsp;</strong>Candidates possessing professional and technical qualifications which
                    are recognised by the Government as equivalent to professional and technical degree would also be
                    eligible for admission to the examination.</p>
                <h3>Q. I have passed MBBS, but not completed internship. Can I appear for UPSC CSE Mains?</h3>
                <p><strong>Answer:&nbsp;</strong>Candidates who have passed the final professional M.B.B.S. or any other
                    Medical Examination but have not completed their internship by the time of submission of their
                    applications for the Civil Services (Main) Examination, will be provisionally admitted to the
                    Examination provided they submit a copy along with their application a copy of certificate from the
                    concerned authority of the University/Institution that they had passed the requisite final
                    professional medical examination, along with their application. In such cases, the candidates will
                    be required to produce at the time of their interview original Degree or a certificate from the
                    concerned competent authority of the University/Institution that they had completed all requirements
                    (including completion of internship) for the award of the Degree.</p>
                <h3>Q. Can I clear IAS exam without attending classroom coaching?</h3>
                <p><strong>Answer:&nbsp;</strong>Yes, you can if you are good at self-study. We are not against
                    classroom coaching. There are good institutes and teachers who help aspirants save a lot of time and
                    effort. But not all coaching institutes provide quality service, so if you wish to join one, do that
                    after proper research. It should also be noted that with the advent of technology, guidance and
                    study materials can be sought online. Our website (Dhyeya IAS) provides free guidance and study
                    materials to lakhs of aspirants who can not afford classroom coaching. You can also learn and
                    compete with thousands of aspirants across India by attempting Dhyeya IAS full length timed online
                    mock test series with negative marking for UPSC Prelims.</p>
                <h3>Q. Will there be an individual cut-off for two papers in Civil Service Prelims?</h3>
                <p><strong>Answer:&nbsp;</strong>The minimum cut off marks for Paper 2 is 33 percent. The Commission may
                    fix a minimum cut-off mark for Paper 1 too.</p>
                <h3>Q. Will there be negative marks or different marks for Preliminary Questions?</h3>
                <p><strong>Answer:&nbsp;</strong>There will be negative marking for incorrect answers for all questions
                    except some of the questions where the negative marking will be inbuilt in the form of different
                    marks being awarded to the most appropriate and not so appropriate answers for such questions.</p>
                <h3>Q. Which stream/subject should I choose for graduation to clear UPSC exam?</h3>
                <p><strong>Answer:&nbsp;</strong>Most of the questions in the general studies paper in Prelims and Mains
                    are related to Humanities background. But it is neither necessary nor advisable to take Humanities
                    as your graduation stream just to clear UPSC exam. Graduation should be based on your interest
                    &ndash; it can be humanities, science, engineering, literature or management. For graduation, select
                    any stream you like to study for 3-4 years. You are free to choose any optional subject for UPSC
                    Mains and it may not be the one you studied for graduation.</p>
                <h3>Will there be minimum qualifying marks for UPSC Mains Compulsory Papers?</h3>
                <p><strong>Answer:&nbsp;</strong>The Commission has the discretion to fix qualifying marks in any or all
                    the subjects of the examination. Since 2015, the minimum mark were 25% for Indian language and
                    English langu age. For GS1, GS2, GS3 and GS4 the minimum marks expected are 10%.</p>
                <h3>Q. Will UPSC deduct marks for bad handwriting?</h3>
                <p><strong>Answer:</strong>&nbsp;If a candidates handwriting is not easily legible, a deduction will be
                    made on this account from the total marks otherwise accruing to him.</p>
                <h3>Q. Will there be minimum qualifying marks for UPSC Interview?</h3>
                <p><strong>Answer:&nbsp;</strong>The interview will carry 275 marks (with no minimum qualifying marks).
                </p>
                <h3>Q. Can candidates use calculators in UPSC Civil Services Exam?</h3>
                <p><strong>Answer:&nbsp;</strong>Not for UPSC Civil Services Preliminary Exam. But candidates will be
                    allowed to use the Scientific (Non-Programmable type) Calculators at the subjective type examination
                    of UPSC, ie Mains Exam. Programmable type calculators will not however be allowed and the use of
                    such calculators shall tantamount to resorting to unfair means by the candidates. Loaning or
                    interchanging of calculators in the Examination Hall is not permitted. It is also important to note
                    that candidates are not permitted to use calculators for answering objective type papers (Test
                    Booklets). They should not, therefore, bring the same inside the Examination Hall.</p>
                <h3>Q. Is coaching important?</h3>
                <p><strong>Answer:&nbsp;</strong>This is one of the most frequently asked questions. UPSC has been
                    constantly changing its pattern over the years to make the civil services exam preparation
                    accessible and possible to students from every set of background. Its Endeavour is to neutralize the
                    effect of coaching so that all candidates come on a equal platform. However, the fact remains
                    &ndash; most of the candidate who succeed have taken coaching at some point of their preparation.
                    This is chiefly due to:</p>
                <ul>
                    <li>The vast syllabus, especially in general studies which the candidates find very difficult to do
                        on their own in a shortest possible period. Moreover, the students are flooded with so many
                        books that they get confused about consulting which ones. Class notes and Material provide a
                        solution to this.
                    </li>
                    <li>The candidates many times in their quest for acquiring knowledge lose track of time. They overdo
                        the studies in some subjects at the cost of others. Coaching institutes cover the syllabus
                        within the time period and thus are sought after.
                    </li>
                </ul>
                <p>However, the need for coaching could be removed wholly if the candidates could get the right study
                    material and the right guidance for the examination.</p>
                <p>A word of caution also needs to be included here. If a candidate has made his mind to enroll himself
                    in any coaching, he must go for the best reputed ones. There are many coaching institutes which
                    advertise very attractively, boasting on claims for producing toppers, which is many times false.
                    The candidates who get attracted eventually land up in these institutes which take a toll on their
                    valuable time, money and energy. The candidates must personally make queries about the coaching
                    institutes, asking the students who were previously enrolled and then, only making right
                    decisions.</p>
                <h3>Q. Should we prepare our own Notes?</h3>
                <p><strong>Answer:&nbsp;</strong>Nothing could be better than this. However, the problem comes when one
                    considers the wide syllabus, which is too vast if we include two optional papers and the general
                    studies which in turn comprises so many subjects.</p>
                <p>One can decide to pair his own notes in those areas in which one is weak, or there are many sources
                    to consult, or on those topics which are very important from the examination point of view.
                    Otherwise, it will be a Herculean task to prepare notes on individual topics.</p>
                <p>But those candidates who have decided to appear in civil services exam during their graduation years
                    may go for this as they have ample time to contribute.</p>
                <h3>Q. How many hours of study are sufficient?</h3>
                <p><strong>Answer:&nbsp;</strong>The answer is as many hours as you can study efficiently. In civil
                    services preparation, there is never a time when one feels that there is nothing more to study. So,
                    what exactly matters is the quality of studying rather quantity of reading. First the difference
                    between "studying" and "reading" should be understood. Reading means mere verbalisation of the
                    written texts. What lacks here is understanding. When understanding adds to "Reading &ndash; it
                    becomes studying". So the issue how many hours you can sit and read the text with understanding also
                    connotes to analysis, deep processing of information, interrelating with your past knowledge base
                    and making a view on the topic. Reading on the other hand is merely a passive activity where
                    involvement of the reader is the least.</p>
                <p>We can thus conclude that studying for 8 hours is many times more fruitful than reading for 16 hours.
                    That is to say "Quality" is more important than the "Quantity" invested.</p>
                <h3>Q. Can an average student also compete with the rest?</h3>
                <p><strong>Answer:</strong>&nbsp;An average student has an added advantage as he knows his limitations.
                    He knows he does not have any time to waste. The intelligent ones feel they have the ability to
                    start late and end early ... and they lose at the end. The tortoise rabbit story is not obsolete
                    yet.</p>
                <p>In fact, most of the students who succeed in the Civil service were very ordinary students in their
                    academics. But they knew, how add "extra" into "ordinary" to become "extraordinary". That extra is
                    provided by their firm<br>determination, an honest self-assessment and foolproof planning followed
                    by strict implementation. In short, the hard work put in by them ultimately lands them at the summit
                    of success.</p>
                <h3>Q. How does a good bio-data matter?</h3>
                <p><strong>Answer:&nbsp;</strong>Bio-data matters during the interview and personality test (i.e. during
                    the third phase). Personality test is based wholly or mostly on the basis of bio-data and the
                    questions are framed on information provided by you. In fact, the-then personality is judged by the
                    interview board on the parameters of suitability of candidates for the job. However, a bad bio-data
                    may give some unfavourable impression on the members of the board and thus, a kind of judgment is
                    formed by them consciously or unconsciously.</p>
                <p>At the same time the members of the board are very experienced and they give full opportunity to
                    candidates to thwart any pre-notions formed by the bio-data. So, the candidates by their performance
                    in the interview have full opportunity to score high in the Interview.</p>
                <p>But, the idea is that those candidates who are in their early academic years should try and see that
                    their biodata is without any shortcomings.</p>
                <h3>Q. Could the civil services questions be answered in the regional languages?</h3>
                <p><strong>Answer:&nbsp;</strong>Yes, aspirants can write answers in any language provided in the eighth
                    schedule of the constitution of India.</p>
                <h3>Q. Preparation for Preliminary and Mains &ndash; Separate or Integrated?</h3>
                <p><strong>Answer:&nbsp;</strong>Can knowledge be compartmentalized? No, in fact it is an integrated
                    whole which gives a comprehensive understanding. Same holds true for the preparation. The whole
                    subject should be understood in its entirety. Only then the requirement for the Preliminary and
                    Mains could be fulfilled through particular emphasis on the orientation. The orientation for Prelims
                    is towards a mix of factual information and understanding with a bias towards the former.</p>
                <h3>Q. When should one take his first attempt irrespective of the fact how many attempts the candidate
                    has?</h3>
                <p><strong>Answer:&nbsp;</strong>Many candidates appear in their first attempt taking it to be a
                    learning experience. This is the biggest mistake they commit. UPSC attempts are very precious ones.
                    Many candidates regret wasting their first attempt and wish if they had one they would have cracked
                    the exam. The candidates must be very serious before appearing in the preliminary exam. They must
                    complete the whole syllabus in their optional as well as general studies. They must check their
                    performance by taking mock tests at home and work out the weak areas. The greatest benefit of the
                    whole exercise is that there is a high probability that they get through the preliminary exam. If
                    unfortunately, they could not, at least they came to know their grey and weak areas on which they
                    need to concentrate. This enlightenment benefits them immensely in their next attempt. Those who
                    waste their first attempt do not come to know about their shortcomings and in this situation may
                    even waste their other attempts.</p>
                <h3>Q. How to select the optionals?</h3>
                <p><strong>Answer:&nbsp;</strong>This has been answered earlier too. The basic criteria are in same
                    sequence of importance.</p>
                <ul>
                    <li>Interest.</li>
                    <li>Availability of study material and guidance.</li>
                    <li>Gap between the optionals during the Mains exam.</li>
                    <li>Scoring optionals &ndash; trend prevailing.</li>
                </ul>
                <h3>Q. Speaking English during an Interview &ndash; How important is it?</h3>
                <p><strong>Answer:&nbsp;</strong>UPSC holds personality tests and interviews in English, Hindi and all
                    other languages in the 8th schedule &ndash; i.e. 22 languages, provided the candidates writes all
                    the papers ()optional and general studies) in the same language. Also, UPSC through the Civil
                    Services exam tries to dig out the best brains in the country, who could contribute in development
                    and progress of the nation. It is thus a wrong notion to hold that the UPSC is language-biased.
                    However, the candidates themselves need to learn English for their own sake as during their long
                    career in civil services they have to come across so many occasions where they will have to
                    communicate with various kind of organisations, institutions, seniors and people and during such a
                    situation, language should not be a limitation.</p>
